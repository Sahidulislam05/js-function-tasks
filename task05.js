function resultReport(marks) {
  if (Array.isArray(marks) == false) {
    return "Invalid";
  }
  if (marks.length === 0) {
    return { finalScore: 0, pass: 0, fail: 0 };
  }
  let sum = 0;
  let passMark = 0;
  let failMark = 0;
  for (let i = 0; i < marks.length; i++) {
    if (marks[i] < 40) {
      failMark++;
    } else {
      passMark++;
    }
    sum += marks[i];
  }

  let score = Math.round(sum / marks.length);

  let result = {
    finalScore: score,
    pass: passMark,
    fail: failMark,
  };

  return result;
}

